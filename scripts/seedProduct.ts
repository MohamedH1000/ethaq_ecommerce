import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();

interface ProductData {
  name: string;
  price: number;
  description?: string;
  images?: string[];
  discount?: number;
  // Add other product fields as needed
}

// Example usage to delete all products from a category
// deleteAllProductsFromCategory(electronicsCategoryId);
async function seedProducts(categoryId: string, products: ProductData[]) {
  try {
    // Verify the category exists
    const category = await prisma.category.findUnique({
      where: { id: categoryId },
    });

    if (!category) {
      console.error(`โ Category with ID ${categoryId} not found`);
      process.exit(1);
    }

    console.log(`โณ Seeding products for category: ${category.name}`);

    // Create products with category relation
    const createdProducts = [];

    for (const product of products) {
      // Check if product already exists in this category
      const existingProduct = await prisma.product.findFirst({
        where: {
          name: product.name,
          categoryId: categoryId,
        },
      });

      if (existingProduct) {
        console.log(`โฉ Skipping existing product: ${product.name}`);
        continue;
      }

      // Create the product if it doesn't exist
      const createdProduct = await prisma.product.create({
        //@ts-ignore
        data: {
          ...product,
          category: {
            connect: { id: categoryId },
          },
        },
      });

      createdProducts.push(createdProduct);
      console.log(`โ Created product: ${product.name}`);
    }

    console.log(`โ Successfully created ${createdProducts.length} products`);
    console.log(
      `โฉ Skipped ${products.length - createdProducts.length} existing products`
    );
    console.log("โจ Products seeded successfully!");

    return createdProducts;
  } catch (error) {
    console.error(
      "๐จ Error seeding products:",
      error instanceof Error ? error.message : error
    );
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

